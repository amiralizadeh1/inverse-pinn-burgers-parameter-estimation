"""
Plot cross-validation results for the inverse PINN Burgers-equation experiment.

This script reads the `results.xlsx` file generated by the main inverse PINN
training script and creates summary plots for:

    - relative L2 error,
    - recovered lambda parameter,
    - supervised test loss,
    - training loss.

Each metric is assumed to be stored fold-wise in columns such as:

    error_fold_0, error_fold_1, error_fold_2
    lambda_fold_0, lambda_fold_1, lambda_fold_2
    loss_test_fold_0, ...
    loss_train_fold_0, ...

For each metric, the script plots the mean trajectory across folds and shades
the ±1 standard-deviation region. An inset plot highlights the final 10
iterations, which is useful for checking convergence behaviour near the end of
optimisation.
"""

import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import matplotlib as mpl
import os
from mpl_toolkits.axes_grid1.inset_locator import inset_axes

# Global plotting style
mpl.rcParams.update({
    'font.size': 15,
    'axes.titlesize': 15,
    'axes.labelsize': 15,
    'xtick.labelsize': 15,
    'ytick.labelsize': 15,
    'legend.fontsize': 15,
    'figure.titlesize': 15,
    'figure.figsize': (6.0, 4.0),
    'figure.dpi': 120,
    'savefig.dpi': 300,
    'savefig.bbox': 'tight',
    'savefig.pad_inches': 0.02,
    'lines.linewidth': 2.0,
    'lines.markersize': 5.5,
    'axes.grid': True,
    'grid.alpha': 0.35,
    'grid.linestyle': '--',
    'grid.linewidth': 0.6,
    'axes.spines.top': True,
    'axes.spines.right': True,
    'xtick.direction': 'out',
    'ytick.direction': 'out',
    'xtick.major.size': 4,
    'ytick.major.size': 4,
    'legend.frameon': False,
    'legend.handlelength': 2.0,
    'image.cmap': 'viridis',
    'figure.autolayout': True,
})

# Define paths
base_path = r"d:\PhD\Implementation\PINN\TensorFlow\Burgers Equation\Revision\general"
output_path = base_path

# Read data
excel_file = os.path.join(base_path, "results.xlsx")
df = pd.read_excel(excel_file)

# Extract fold data for each metric
def extract_fold_data(df, metric_prefix):
    """Extract data for all folds of a given metric"""
    fold_values = []
    fold = 0
    while True:
        col_name = f"{metric_prefix}_fold_{fold}"
        if col_name in df.columns:
            fold_values.append(df[col_name].values)
            fold += 1
        else:
            break
    return np.array(fold_values) if fold_values else None

# Extract data for each metric
error_data = extract_fold_data(df, "error")
lambda_data = extract_fold_data(df, "lambda")
loss_test_data = extract_fold_data(df, "loss_test")
loss_train_data = extract_fold_data(df, "loss_train")

# Create comparison plot
def create_comparison_plot(fold_data, metric_name, ylabel, output_file):
    """Create a comparison plot with mean and std, with inset for last 10 iterations"""
    fig, ax = plt.subplots(figsize=(12, 7))
    
    colors = ['#1f77b4']
    light_colors = ['#aec7e8']
    hatches = ['///']
    
    if metric_name == 'Error':
        ax.tick_params(labelsize=18)
        ax.xaxis.label.set_fontsize(18)
        ax.yaxis.label.set_fontsize(18)
    
    # Calculate mean and std across folds
    mean_vals = np.mean(fold_data, axis=0)
    std_vals = np.std(fold_data, axis=0)
    
    iterations = np.arange(len(mean_vals))
    
    # Plot mean line
    ax.plot(iterations, mean_vals, label=metric_name, 
            color=colors[0], linewidth=2)
    
    # Add std shadow
    ax.fill_between(iterations, mean_vals - std_vals, mean_vals + std_vals,
                    alpha=0.3, color=light_colors[0],
                    hatch=hatches[0], edgecolor='none')
    
    ax.set_xlabel("Iteration")
    ax.set_ylabel(ylabel)
    ax.legend(loc='best')
    ax.grid(True, alpha=0.3)
    
    # Add horizontal dashed line at 0.005/π for lambda plot
    if metric_name == 'Lambda':
        threshold = 0.005 / np.pi
        ax.axhline(y=threshold, color='red', linestyle='--', linewidth=2, label=f'0.005/π = {threshold:.6f}')
    
    # Create inset for last 10 iterations
    inset_loc = 'upper center' if metric_name == 'Error' else 'center'
    axins = inset_axes(ax, width="50%", height="50%", loc=inset_loc)
    
    last_10_mean = mean_vals[-10:]
    last_10_std = std_vals[-10:]
    last_10_iter = np.arange(len(mean_vals) - 10, len(mean_vals))
    
    axins.plot(last_10_iter, last_10_mean, color=colors[0], linewidth=2)
    axins.fill_between(last_10_iter, last_10_mean - last_10_std, 
                      last_10_mean + last_10_std, alpha=0.3, 
                      color=light_colors[0],
                      hatch=hatches[0], edgecolor='none')
    
    # Add horizontal dashed line in inset for lambda plot
    if metric_name == 'Lambda':
        threshold = 0.005 / np.pi
        axins.axhline(y=threshold, color='red', linestyle='--', linewidth=2)
    
    plt.tight_layout()
    plt.savefig(output_file, dpi=300, bbox_inches='tight')
    plt.close()
    print(f"Saved: {output_file}")

# Create all comparison plots
if error_data is not None:
    create_comparison_plot(error_data, "Error", "Error", 
                          os.path.join(output_path, "error_general.png"))

if lambda_data is not None:
    create_comparison_plot(lambda_data, "Lambda", "Lambda Value", 
                          os.path.join(output_path, "lambda_general.png"))

if loss_test_data is not None:
    create_comparison_plot(loss_test_data, "Test Loss", "Test Loss", 
                          os.path.join(output_path, "loss_test_general.png"))

if loss_train_data is not None:
    create_comparison_plot(loss_train_data, "Training Loss", "Training Loss", 
                          os.path.join(output_path, "loss_train_general.png"))

print("All plots created successfully!")
